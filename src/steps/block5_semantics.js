const { askSonnet } = require('../claude');
const { STEPS } = require('../state');

async function runBlock5(ctx, session) {
  await ctx.reply(
    'Шаг 5 — Семантическое ядро\n\n' +
    'Зачем: это словарь твоей аудитории — слова и фразы которыми люди реально думают и говорят о твоём продукте. ' +
    'На их основе строятся все заголовки, хуки и статьи. ' +
    'Плюс — оптимизация под Google и AI-поиск (ChatGPT, Perplexity), чтобы тебя находили везде где ищут.\n\n' +
    'Собираю ключевые слова... ~2 минуты.'
  );

  session.semanticCore = await askSonnet(`
Ты — SEO и GEO специалист (оптимизация под поисковики и AI-ассистентов).

БИЗНЕС: ${session.businessProfile}
АУДИТОРИЯ: ${session.audience}
КАСТДЕВ (слова аудитории): ${session.castdev}
${session.reviewSitePhrases ? `${session.reviewSitePhrases}\n` : ''}РЕГИОН: ${session.regionLabel}

Создай семантическое ядро в виде таблицы. Используй слова и фразы которыми РЕАЛЬНО говорит целевая аудитория.

Учти что контент должен находиться в:
- Google / Bing (SEO)
- ChatGPT, Perplexity, Claude, Gemini (GEO — AI-поиск) — для этого нужны чёткие факты и определения

---

## ТАБЛИЦА 1 — КЛЮЧЕВЫЕ СЛОВА
(одно-два слова, высокая частотность)
| Слово | Частота использования | Для какого сегмента ЦА |
|---|---|---|
(минимум 20 слов)

## ТАБЛИЦА 2 — СЛОВОСОЧЕТАНИЯ И ДЛИННЫЕ ФРАЗЫ
(3-6 слов, конкретные запросы)
| Фраза | Намерение пользователя | Этап воронки |
|---|---|---|
(минимум 20 фраз)

## ТАБЛИЦА 3 — ЗАГОЛОВКИ ДЛЯ КОНТЕНТА
(готовые заголовки для статей, Reels, каруселей)
| Заголовок | Формат контента | Сегмент ЦА |
|---|---|---|
(минимум 15 заголовков)

## GEO-КЛЮЧИ (для AI-поиска)
Список из 10 вопросов/фраз которые люди задают AI-ассистентам когда ищут этот продукт/услугу

## ТАБЛИЦА 4 — ГОЛОС КЛИЕНТА ИЗ РЕАЛЬНЫХ ОТЗЫВОВ
(только если есть данные из отзывов выше — используй их напрямую, без додумывания)
| Цитата из отзыва | Что это говорит о потребности | Как использовать в контенте |
|---|---|---|
(10-15 строк — реальные слова покупателей, не маркетинговые формулировки)
  `, 6500);

  // Извлекаем только таблицу заголовков — она будет использована в статьях как темы
  session.headlines = await askSonnet(`
Из семантического ядра ниже выпиши ТОЛЬКО таблицу заголовков для контента (ТАБЛИЦА 3).
Пиши БЕЗ markdown-форматирования — только чистый текст.

СЕМАНТИЧЕСКОЕ ЯДРО:
${session.semanticCore}

Выведи список заголовков в формате:
1. [заголовок] | [формат контента] | [сегмент ЦА]
2. [заголовок] | [формат контента] | [сегмент ЦА]
...
(все заголовки из таблицы 3, ничего не добавляй от себя)
  `, 1500);

  session.step = STEPS.BLOCK3_INPUT;
  await ctx.reply('✅ Блок 5 — семантическое ядро готово (сохранено в отчёт). Анализирую конкурентов...');
  return true;
}

module.exports = { runBlock5 };
