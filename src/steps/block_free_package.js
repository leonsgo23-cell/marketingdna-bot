const FREE_TIMEOUT = 120000; // 2 мин — достаточно для Sonnet, в 2.5x быстрее старых 300s

const LEGAL_RULES = `ПРАВОВЫЕ ОГРАНИЧЕНИЯ ЕС/ЛАТВИЯ (обязательно соблюдать):
1. БЕЗ гарантий результата — нельзя "удвоите продажи", "гарантированный рост X%". Можно: "помогает привлекать", "способствует росту".
2. БЕЗ искусственной срочности — "только сегодня/последний шанс" только при реальном ограничении.
3. Отзывы — обезличенно, без конкретных имён и цифр заработка без явного согласия реального человека.
4. БЕЗ сравнений с конкурентами по цифрам без доказательств.
5. Мотивация через возможности, не страх.`;

function askFree(prompt, maxTokens, label) {
  return ask(prompt, { model: SONNET, maxTokens, timeoutMs: FREE_TIMEOUT, label });
}

const PERSONAL_BRAND_KEYWORDS = [
  'коуч', 'консульт', 'тренер', 'спикер', 'психолог', 'наставник',
  'эксперт', 'терапевт', 'ментор', 'репетитор', 'обучени', 'образовани',
  'coach', 'consult', 'trainer', 'speaker', 'expert', 'mentor',
];

function detectPersonalBrand(description, answers, contentFormat) {
  // fmt_person_lead = человек главный герой → Пакет Б (личный бренд)
  if (contentFormat === 'fmt_person_lead') return true;

  // fmt_person_support = человек второстепенный / fmt_product = без человека → Пакет В
  if (contentFormat === 'fmt_person_support') return false;
  if (contentFormat === 'fmt_product') return false;

  // fmt_unsure или не задан — анализируем по ключевым словам
  const allText = [description || '', ...(answers || []).map(a => a.answer)]
    .join(' ')
    .toLowerCase();
  return PERSONAL_BRAND_KEYWORDS.some(kw => allText.includes(kw));
}

