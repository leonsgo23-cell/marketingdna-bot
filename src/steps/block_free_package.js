const { askSonnet } = require('../claude');
const { getLangInstruction } = require('../lang');

const PERSONAL_BRAND_KEYWORDS = [
  'коуч', 'консульт', 'тренер', 'спикер', 'психолог', 'наставник',
  'эксперт', 'терапевт', 'ментор', 'репетитор', 'обучени', 'образовани',
  'coach', 'consult', 'trainer', 'speaker', 'expert', 'mentor',
];

function detectPersonalBrand(description, answers, contentFormat) {
  // fmt_person_lead = человек главный герой → Пакет Б (личный бренд)
  if (contentFormat === 'fmt_person_lead') return true;

  // fmt_person_support = человек второстепенный / fmt_product = без человека → Пакет В
  if (contentFormat === 'fmt_person_support') return false;
  if (contentFormat === 'fmt_product') return false;

  // fmt_unsure или не задан — анализируем по ключевым словам
  const allText = [description || '', ...(answers || []).map(a => a.answer)]
    .join(' ')
    .toLowerCase();
  return PERSONAL_BRAND_KEYWORDS.some(kw => allText.includes(kw));
}

async function generateFreePackage(triggerData, enrichedData = {}) {
  const { description, answers, contentFormat, contentLanguage, ctaPreference, leadMagnet } = triggerData;
  const qa = (answers || []).map(a => `Вопрос: ${a.question}\nОтвет: ${a.answer}`).join('\n\n');
  const isPersonalBrand = detectPersonalBrand(description, answers, contentFormat);
  const langInstruction = getLangInstruction(contentLanguage);

  const ctaInstruction = ctaPreference === 'direct_magnet'
    ? `CTA: клиент готов общаться в директе. Лид-магнит: "${leadMagnet}". Используй призывы типа "напиши слово X в директ — пришлю [лид-магнит]".`
    : ctaPreference === 'direct_only'
    ? `CTA: клиент готов отвечать в директе, но лид-магнита нет. Призывы "напиши в директ — расскажу подробнее".`
    : `CTA: клиент НЕ ведёт директ. Только комментарии / ссылка в bio / форма на сайте. НЕ использовать "напиши в директ".`;

  const biz = enrichedData.businessProfile
    ? enrichedData.businessProfile.slice(0, 1500)
    : `${description}\n\n${qa}`.slice(0, 1500);
  const aud = (enrichedData.audience || '').slice(0, 1000);
  const comp = (enrichedData.competitorBrief || '').slice(0, 800);

  const compSection = comp
    ? `\nАНАЛИЗ КОНКУРЕНТОВ (незакрытые темы и возможности):\n${comp}`
    : '';
  const audSection = aud ? `\nПОРТРЕТ АУДИТОРИИ:\n${aud}` : '';

  // 1. Контент-план 7 дней
  const contentPlan = await askSonnet(`
Ты — контент-стратег. Создай персональный контент-план на 7 дней.
Пиши БЕЗ markdown-форматирования (никаких **, *, #, _) — только чистый текст.
${langInstruction}

ПРОФИЛЬ БИЗНЕСА:
${biz}
${audSection}
${compSection}

Для каждого дня укажи:
ДЕНЬ [N]:
Платформа: [Instagram / TikTok / LinkedIn]
Формат: [Reel / Карусель / Пост / Stories]
Тема: [конкретная тема — из болей аудитории или незакрытых тем конкурентов]
Хук (первые слова): [цепляющее начало]
Суть контента: [2-3 предложения о чём пост]
CTA: [призыв к действию]

Используй разные форматы. Каждая тема — конкретная, под реальные боли этой аудитории.
ПРАВИЛО CTA: ${ctaInstruction}
  `, 2500);

  // 2. SEO-статья
  const seoArticle = await askSonnet(`
Ты — SEO-копирайтер. Напиши одну полноценную статью для сайта.
Пиши БЕЗ markdown-форматирования — только чистый текст.
${langInstruction}

ПРОФИЛЬ БИЗНЕСА:
${biz}
${audSection}
${compSection}

Структура:
- SEO-заголовок (под реальный запрос аудитории)
- Введение (2-3 абзаца, начни с боли читателя)
- 3-4 раздела с подзаголовками
- Заключение + CTA
- Мета-описание (150-160 знаков)

Объём: 600-800 слов. Язык — простой, как говорит целевая аудитория.
Если есть незакрытые темы конкурентов — статья должна закрыть одну из них глубже чем они.
  `, 2000);

  // 3. Сценарий ролика
  const videoScript = await askSonnet(`
Ты — сценарист Reels и TikTok. Напиши один готовый сценарий ролика (60-90 сек).
Пиши БЕЗ markdown-форматирования — только чистый текст.
${langInstruction}

ПРОФИЛЬ БИЗНЕСА:
${biz}
${audSection}

Выбери самую острую боль аудитории из портрета.

СЦЕНАРИЙ РОЛИКА:
Платформа: [Instagram Reels / TikTok]
Тема:
Хук (первые 3 секунды — текст на экране + что говоришь):
Шаг 1:
Шаг 2:
Шаг 3:
Концовка + CTA:
Текст на обложке: [5-7 слов]

Пиши конкретно — точные фразы. Не "расскажи о себе", а точный текст.
ПРАВИЛО CTA: ${ctaInstruction}
  `, 1200);

  // 4. Сценарий карусели
  const carouselScript = await askSonnet(`
Ты — контент-стратег. Напиши сценарий одной карусели (5 слайдов) для Instagram или TikTok.
Пиши БЕЗ markdown-форматирования — только чистый текст.
${langInstruction}

ПРОФИЛЬ БИЗНЕСА:
${biz}
${audSection}
${compSection}

Выбери боль или вопрос который волнует аудиторию больше всего — или тему которую конкуренты не закрывают.

СЦЕНАРИЙ КАРУСЕЛИ:
Тема:
Платформа: [Instagram / TikTok / LinkedIn]

Слайд 1 (обложка — цепляющий заголовок): [текст]
Слайд 2: [текст + пояснение]
Слайд 3: [текст + пояснение]
Слайд 4: [текст + пояснение]
Слайд 5 (финал + CTA): [текст + призыв]

Визуальный стиль: [2-3 слова — минимализм / яркий / профессиональный и т.д.]
  `, 800);

  // 5. Пример обложки для видео
  const coverExample = await askSonnet(`
Ты — дизайнер обложек для соцсетей. Создай один пример обложки для Reels или TikTok СТРОГО для этого бизнеса.
Пиши БЕЗ markdown-форматирования — только чистый текст.
${langInstruction}

ПРОФИЛЬ БИЗНЕСА:
${biz}
${audSection}

Заголовок на обложке — 5-7 слов, цепляющий, строго по теме этого бизнеса:
[заголовок]

Подзаголовок (если нужен):
[подзаголовок или "без подзаголовка"]

Визуальный стиль:
[фон, цвета, настроение — 2-3 слова]

Почему это остановит взгляд:
[1-2 предложения]
  `, 700);

  // 6. Пример фото для Instagram
  const photoExample = await askSonnet(`
Ты — контент-менеджер. Создай готовый пост-фото для Instagram.
Пиши БЕЗ markdown-форматирования — только чистый текст.
${langInstruction}

ПРОФИЛЬ БИЗНЕСА:
${biz}
${audSection}

Подпись к посту:
[2-3 живых предложения от имени бизнеса — цепляющее начало, суть, CTA]

Хэштеги:
[8-10 релевантных хэштегов через пробел]

Почему это зайдёт аудитории:
[2-3 предложения — объяснение психологии и логики этого контента]

Как разместить:
[2-3 практических шага — открыть Instagram, нажать +, выбрать фото, вставить подпись, опубликовать]
  `, 900);

  return { contentPlan, seoArticle, videoScript, carouselScript, coverExample, photoExample, isPersonalBrand };
}

