// Промпт для двухнедельного анализа
function buildAnalyticsPrompt(session, analyticsData, publishedContent) {
  const niche = session.businessProfile?.slice(0, 200) || '';
  const goal  = session.contentGoal || 'привлечение новых клиентов';
  const lang  = session.contentLanguage || 'ru';

  return `
Ты — аналитик контента. Проведи двухнедельный анализ эффективности контента для бизнеса.

ЦЕЛЬ КОНТЕНТА: ${goal}
БИЗНЕС: ${niche}
ЯЗЫК: ${lang}

ОПУБЛИКОВАННЫЙ КОНТЕНТ (последние 15 дней):
${publishedContent}

ДАННЫЕ АНАЛИТИКИ:
${analyticsData}

НОРМАТИВЫ ДЛЯ АНАЛИЗА:
- Хороший охват поста: от 1000 для малого бизнеса
- Хороший % досмотра Reels: от 40%
- Хорошие сохранения поста: от 30
- Хорошая вовлечённость: от 3%

ЗАДАЧА:
1. Выяви что сработало хорошо (топ 3 материала и почему)
2. Выяви что не сработало (и предположи почему)
3. Определи 2-3 корректировки для следующих 2 недель:
   - Какие темы усилить
   - Какие форматы изменить
   - Что убрать или заменить
4. Дай конкретные рекомендации по времени и частоте публикаций если видишь паттерн

ВАЖНО:
- Не говори что работа была плохой — говори что можно улучшить
- Опирайся только на данные — не додумывай
- Пиши кратко и по делу — менеджер проверит перед отправкой клиенту

Формат ответа:
ЧТО СРАБОТАЛО:
[список]

ЧТО МОЖНО УЛУЧШИТЬ:
[список]

КОРРЕКТИРОВКИ НА СЛЕДУЮЩИЕ 2 НЕДЕЛИ:
[конкретные изменения в темах/форматах]

РЕКОМЕНДАЦИИ ПО ПУБЛИКАЦИИ:
[время, частота, если есть данные]
`;
}

